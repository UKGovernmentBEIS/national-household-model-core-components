(template ehs.set-tariff [@tariff @var]
    (action.case
        (when
            (house.static-property-is
                name: @var
                equal-to: (ehs.mop.direct-debit))
            (action.set-tariffs
                (join "#" @tariff "-" DirectDebit)))
                
        (when
            (house.static-property-is
                name: @var
                equal-to: (ehs.mop.standard-credit))
            (action.set-tariffs
                (join "#" @tariff "-" StandardCredit)))
                
        (when
            (house.static-property-is
                name: @var
                equal-to: (ehs.mop.prepay-meter))
            (action.set-tariffs
                (join "#" @tariff "-" PrepayMeter)))
                
        default: (house.flag set-tariff-failed)))

(template ehs.gas-mop-is [@val]
    (house.static-property-is
        name: gasMOP
        equal-to: @val))
        
(template ehs.elec-mop-is [@val]
    (house.static-property-is
        name: elecMOP
        equal-to: @val))
        
(template ehs.on-gas []
    (none
        (ehs.off-gas)))

(template ehs.off-gas []
    (ehs.gas-mop-is 
        val: (ehs.mop.no-gas)))

  (template do.if [@condition @action]
            (action.case
             (when
               @condition
               @action)
             default: (action.do-nothing)))
  
  (template do.until-limit [@limit @1]
            (do.if
             condition: (> @limit 0)
             action: (do
                      @1
                      (decrease @limit 1))))
  
  (template hack.set-global [@1 @2]
            ; Hack hack hack
            (apply
             to: (sample 1)
             (do
              (set @1 @2))))
  
  (template setup-dual-fuel [@mop @ehsmop @fraction]
            ; For a given method of payment,
            ; the number of dwellings which will have dual fuel is the number of gas customers using that method of payment multiplied by a percentage.
            ; However, we only want to apply this to people whose gas-mop is the same as their elec-mop.
  
            (set
             ; First count the total number of gas customers using this method of payment.
             (def (join gas- @mop) on: Simulation)
             (summarize
              group: (group.filter
                      (ehs.gas-mop-is val: @ehsmop))
              (aggregate.count)))
  
             (hack.set-global
              ; Multiply that by the fraction of gas customers using this mop which we think should be dual-fuel.
              (def (join dual-fuel- @mop) on: Simulation)
              (* @fraction (join #gas- @mop)))
  
             (apply
              ; Put that number of people on the tariff, but only where gas-mop is the same as elec-mop.
              to: (filter
                   (all
                    (ehs.gas-mop-is val: @ehsmop)
                    (ehs.elec-mop-is val: @ehsmop)))
              (do.until-limit
               limit: (join #dual-fuel- @mop)
               (action.set-tariffs
                update-flags: dual-fuel
                (join #Dual-fuel- @mop)))))
  
  
  
  (template init-tariffs []
  
      (on.dates
          (scenario-start)
          
          (apply
           to: (filter
            (ehs.off-gas))
           (ehs.set-tariff
            tariff: Economy7
            var: elecMOP))
  
          (setup-dual-fuel
           mop: DirectDebit
           ehsmop: (ehs.mop.direct-debit)
           fraction: 62%)
  
          (setup-dual-fuel
           mop: StandardCredit
           ehsmop: (ehs.mop.standard-credit)
           fraction: 43%)
  
          (setup-dual-fuel
           mop: PrepayMeter
           ehsmop: (ehs.mop.prepay-meter)
           fraction: 51%)
  
          (apply
           to: (filter
                (none
                 (ehs.off-gas)
                 (house.flags-match dual-fuel)))
           (do
            (ehs.set-tariff
             tariff: MainsGas
             var: gasMOP)
            (ehs.set-tariff
             tariff: Standard-electricity
             var: elecMOP)))))

